		<em><h4>Florida Space Grant Consortium Hybrid Rocket Competition</h4></em>
		<p style="margin-left:15px;">
			Each year SEDS-UCF participates in FSGC's Hybrid Rocket Competition. Our "mini hybrid team" is in 
			an effort to give our members more experience in the design, simulation, and flight of smaller-scale 
			rockets, as we launch our own G and H-motor mini-hybrid rockets monthly at the NEFAR rocket launches.
			The objective of the competition is to build and launch a hybrid powered rocket. There are two 
			categories of competition to choose from. The first category consists of launching a hybrid 
			rocket to the maximum altitude. The second category challenges the teams to fly their rocket 
			closest to 2000 feet in altitude.
		</p>
		
